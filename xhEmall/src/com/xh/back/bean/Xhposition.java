package com.xh.back.bean;

public class Xhposition {
    private Integer positionId;
    
    private String  positionName;
    
    private Double  positionWidth;
    
    private Double  positionHeight;
    
    private int positionFlag;

	public Integer getPositionId() {
		return positionId;
	}

	public void setPositionId(Integer positionId) {
		this.positionId = positionId;
	}

	public String getPositionName() {
		return positionName;
	}

	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}

	public Double getPositionWidth() {
		return positionWidth;
	}

	public void setPositionWidth(Double positionWidth) {
		this.positionWidth = positionWidth;
	}

	public Double getPositionHeight() {
		return positionHeight;
	}

	public void setPositionHeight(Double positionHeight) {
		this.positionHeight = positionHeight;
	}

	public int getPositionFlag() {
		return positionFlag;
	}

	public void setPositionFlag(int positionFlag) {
		this.positionFlag = positionFlag;
	}
    
    
}
